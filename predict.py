import pandas as pd
import joblib

model = joblib.load("traffic_model.pkl")

junction = int(input("Enter Junction: "))
year = int(input("Enter Year: "))
month = int(input("Enter Month: "))
day = int(input("Enter Day: "))
hour = int(input("Enter Hour: "))

date = pd.Timestamp(
    year=year,
    month=month,
    day=day
)

day_of_week = date.dayofweek

input_data = [[
    junction,
    hour,
    day,
    month,
    year,
    day_of_week
]]

prediction = model.predict(input_data)

vehicles = int(prediction[0])

print("\nPredicted Vehicles =",vehicles)

if vehicles < 20:
    print("Traffic Status : Low")

elif vehicles < 50:
    print("Traffic Status : Medium")

else:
    print("Traffic Status : High")