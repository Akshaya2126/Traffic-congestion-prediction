import streamlit as st
import pandas as pd
import joblib
import plotly.express as px

# Load model
model = joblib.load("traffic_model.pkl")

# Load dataset
df = pd.read_csv("traffic.csv")

df["DateTime"] = pd.to_datetime(df["DateTime"])

st.set_page_config(
    page_title="Traffic Prediction",
    layout="wide"
)

st.title("🚦 Smart Traffic Congestion Prediction")

st.sidebar.header("Prediction Input")

junction = st.sidebar.selectbox(
    "Junction",
    sorted(df["Junction"].unique())
)

year = st.sidebar.number_input(
    "Year",
    min_value=2015,
    max_value=2030,
    value=2017
)

month = st.sidebar.slider(
    "Month",
    1,
    12,
    1
)

day = st.sidebar.slider(
    "Day",
    1,
    31,
    1
)

hour = st.sidebar.slider(
    "Hour",
    0,
    23,
    8
)

if st.sidebar.button("Predict Traffic"):

    date = pd.Timestamp(
        year=year,
        month=month,
        day=day
    )

    day_of_week = date.dayofweek

    prediction = model.predict([[
        junction,
        hour,
        day,
        month,
        year,
        day_of_week
    ]])

    vehicles = int(prediction[0])

    st.subheader("Prediction Result")

    st.metric(
        "Predicted Vehicles",
        vehicles
    )

    if vehicles < 20:
        st.success("Low Traffic")

    elif vehicles < 50:
        st.warning("Medium Traffic")

    else:
        st.error("High Traffic")

# Dashboard Section

st.subheader("Traffic Dataset Preview")

st.dataframe(df.head())

# Vehicles by Hour

hourly = df.copy()

hourly["Hour"] = hourly["DateTime"].dt.hour

hourly_avg = (
    hourly.groupby("Hour")["Vehicles"]
    .mean()
    .reset_index()
)

fig1 = px.line(
    hourly_avg,
    x="Hour",
    y="Vehicles",
    title="Average Vehicles by Hour"
)

st.plotly_chart(fig1,use_container_width=True)

# Junction Comparison

junction_avg = (
    df.groupby("Junction")["Vehicles"]
    .mean()
    .reset_index()
)

fig2 = px.bar(
    junction_avg,
    x="Junction",
    y="Vehicles",
    title="Average Traffic per Junction"
)

st.plotly_chart(fig2,use_container_width=True)

# Monthly Traffic

df["Month"] = df["DateTime"].dt.month

monthly = (
    df.groupby("Month")["Vehicles"]
    .mean()
    .reset_index()
)

fig3 = px.line(
    monthly,
    x="Month",
    y="Vehicles",
    title="Monthly Traffic Trend"
)

st.plotly_chart(fig3,use_container_width=True)
